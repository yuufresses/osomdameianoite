function fase1() 
{

    // Converte para minúsculas
    // .value.toLowerCase(); 

    var textoJogador = document.querySelector("#texto").value.toLowerCase();
    var palavraChave = "eu era determinado".toLowerCase();

    if (textoJogador === palavraChave) 
    {
        alert("Parabens!");
        window.location.href = "1eueradeterminado.html";
    } 
    else 
    {
        alert("Você Errou!");
    }
}

